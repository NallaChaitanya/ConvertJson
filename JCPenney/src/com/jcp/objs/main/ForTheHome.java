package com.jcp.objs.main;

public class ForTheHome {
	
	private Furniture Furniture;
	
	private HomeInspiration HomeInspiration;
	
	private Mattresses Mattresses;
	
	private Windows Windows;

	public Furniture getFurniture() {
		return Furniture;
	}

	public void setFurniture(Furniture furniture) {
		Furniture = furniture;
	}

	public HomeInspiration getHomeInspiration() {
		return HomeInspiration;
	}

	public void setHomeInspiration(HomeInspiration homeInspiration) {
		HomeInspiration = homeInspiration;
	}

	public Mattresses getMattresses() {
		return Mattresses;
	}

	public void setMattresses(Mattresses mattresses) {
		Mattresses = mattresses;
	}

	public Windows getWindows() {
		return Windows;
	}

	public void setWindows(Windows windows) {
		Windows = windows;
	}
	
	

}
